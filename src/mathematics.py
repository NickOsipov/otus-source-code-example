from typing import List


def sum_array(array: List[int, float]) -> int:
    """
    Function to sum an array of integers or floats.

    Parameters
    ----------
    array : List[int, float]
        Input array of integers or floats.

    Returns
    -------
    int
        Sum of the elements in the array.
    """

    return sum(array)
